import java.util.*;
import java.lang.String;
class Sample	
{
int a;
double b;
boolean c;
String d;
}
class Instancevariables
{
public static void main(String args[])
{
Sample s1=new Sample();
System.out.println(s1.a);
System.out.println(s1.b);
System.out.println(s1.c);
System.out.println(s1.d);
}
}